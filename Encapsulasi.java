package UTS4;

public class Encapsulasi {
  private String nama;
private String npm;


public Encapsulasi (String n, String npm) {
this.nama = n;
this.npm = npm;
}

public String tampilkanNama() {
return nama;
}

public String tampilkanNpm() {
return npm;
}
}

